<script setup>
import { ref, computed, watch, onMounted, onBeforeUnmount } from 'vue'
import { useI18n } from 'vue-i18n'
import { useVuelidate } from '@vuelidate/core'
import { required, maxLength } from '@vuelidate/validators'
import { useCustomerStore } from '@/stores/customerStore'

// Components
import TabView from 'primevue/tabview'
import TabPanel from 'primevue/tabpanel'
import InputText from 'primevue/inputtext'
import InputNumber from 'primevue/inputnumber'
import Calendar from 'primevue/calendar'
import Dropdown from 'primevue/dropdown'
import AutoComplete from 'primevue/autocomplete'
import Button from 'primevue/button'
import DataTable from 'primevue/datatable'
import Column from 'primevue/column'
import Dialog from 'primevue/dialog'
import Textarea from 'primevue/textarea'
import Tooltip from 'primevue/tooltip'

// Props & Emits
const props = defineProps({
  order: { type: Object, default: () => ({}) },
  mode: { type: String, default: 'add' },
  loading: { type: Boolean, default: false }
})

const emit = defineEmits(['save', 'cancel', 'save-and-add'])

// Composables
const { t } = useI18n()
const customerStore = useCustomerStore()

// Reactive Data
const activeTab = ref(0)
const localOrder = ref(initializeOrder())
const showUnsavedDialog = ref(false)
const hasUnsavedChanges = ref(false)
const saveLoading = ref(false)
const saveAndAddLoading = ref(false)
const draftLoading = ref(false)
const customerSuggestions = ref([])
const filteredCountries = ref([])

// Form validation rules
const rules = {
  orderNumber: { required, maxLength: maxLength(50) },
  customer: { required },
  status: { required },
  total: { required }
}

const v$ = useVuelidate(rules, localOrder)

// Options
const statusOptions = [
  { label: t('pending'), value: 'Pending' },
  { label: t('fulfilled'), value: 'Fulfilled' },
  { label: t('cancelled'), value: 'Cancelled' }
]

const countryOptions = [
  'Tunisia', 'France', 'United States', 'Canada', 'Germany', 'United Kingdom'
]

// Computed Properties
const formMode = computed(() => props.mode)

const formProgress = computed(() => {
  let progress = 0
  const fields = [
    localOrder.value.orderNumber,
    localOrder.value.customer,
    localOrder.value.status,
    localOrder.value.total,
    localOrder.value.date
  ]
  
  progress += fields.filter(field => field && field !== '').length * 15
  
  if (localOrder.value.items.length > 0) progress += 10
  if (localOrder.value.shippingAddress.street) progress += 5
  if (localOrder.value.shippingAddress.city) progress += 5
  if (localOrder.value.shippingAddress.postalCode) progress += 5
  if (localOrder.value.shippingAddress.country) progress += 5
  if (localOrder.value.notes) progress += 10
  
  return Math.min(progress, 100)
})

// Methods
function initializeOrder() {
  return {
    id: null,
    orderNumber: '',
    customer: '',
    date: new Date().toISOString(),
    total: 0.0,
    status: 'Pending',
    items: [],
    shippingAddress: {
      street: '',
      city: '',
      postalCode: '',
      country: ''
    },
    notes: '',
    ...props.order
  }
}

function updateProgress() {
  // Progress is handled by computed property
}

function previewOrder() {
  console.log('Preview order:', localOrder.value)
}

async function saveAsDraft() {
  draftLoading.value = true
  try {
    localOrder.value.status = 'Pending'
    await validateAndSave()
  } finally {
    draftLoading.value = false
  }
}

async function validateAndSave() {
  const isValid = await v$.value.$validate()
  if (!isValid) {
    const firstError = v$.value.$errors[0]
    console.error('Validation error:', firstError.$message)
    return
  }

  saveLoading.value = true
  try {
    await emit('save', localOrder.value)
    hasUnsavedChanges.value = false
  } finally {
    saveLoading.value = false
  }
}

async function saveAndAddAnother() {
  saveAndAddLoading.value = true
  try {
    await validateAndSave()
    emit('save-and-add')
    resetForm()
  } finally {
    saveAndAddLoading.value = false
  }
}

function resetForm() {
  localOrder.value = initializeOrder()
  v$.value.$reset()
  activeTab.value = 0
}

function handleCancel() {
  if (hasUnsavedChanges.value) {
    showUnsavedDialog.value = true
  } else {
    emit('cancel')
  }
}

function discardChanges() {
  showUnsavedDialog.value = false
  emit('cancel')
}

async function saveBeforeLeave() {
  showUnsavedDialog.value = false
  await validateAndSave()
  emit('cancel')
}

function searchCustomers(event) {
  const query = event.query.toLowerCase()
  customerSuggestions.value = customerStore.customers.filter(c => 
    c.name.toLowerCase().includes(query) || c.email.toLowerCase().includes(query)
  )
}

function searchCountries(event) {
  filteredCountries.value = countryOptions.filter(country => 
    country.toLowerCase().includes(event.query.toLowerCase())
  )
}

function addItem() {
  if (!localOrder.value.items) {
    localOrder.value.items = []
  }
  localOrder.value.items.push({
    productId: Date.now(),
    name: '',
    quantity: 1,
    price: 0.0
  })
  updateProgress()
}

function removeItem(index) {
  localOrder.value.items.splice(index, 1)
  updateProgress()
}

// Watch for changes
watch(localOrder, () => {
  hasUnsavedChanges.value = true
}, { deep: true })

// Lifecycle Hooks
onMounted(() => {
  window.addEventListener('beforeunload', handleBeforeUnload)
})

onBeforeUnmount(() => {
  window.removeEventListener('beforeunload', handleBeforeUnload)
})

function handleBeforeUnload(e) {
  if (hasUnsavedChanges.value) {
    e.preventDefault()
    e.returnValue = t('unsavedChangesWarning')
    return t('unsavedChangesWarning')
  }
}
</script>

